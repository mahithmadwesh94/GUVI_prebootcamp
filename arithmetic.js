//Given two numbers and perform all arithmetic operations.

function arithmeticOperations(num1,num2){
  
  console.log('Product: '+num1*num2);

  console.log('Add: ',num1+num2);

  console.log('Subtract: ',num1-num2);

  console.log('Divie: ',num1/num2);

  console.log('Modulus: ',num1%num2);

  console.log('Pre Increment: ',++num1);

  console.log('Post Increment: ',num1++);

  console.log('Pre Decrement: ',--num2);

  console.log('Post Decrement: ',num2--);
   
}
